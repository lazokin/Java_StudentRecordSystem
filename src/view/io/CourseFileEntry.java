package view.io;

public class CourseFileEntry {

    private String runNumber;
    private String instructorJP;
    private String instructorOOD;
    private String instructorST;
    private String instructorJ2EE;
    private String instructorSA;
    private String instructorDP;

    public CourseFileEntry() {
        super();
    }

    public String getRunNumber() {
        return runNumber;
    }

    public String getInstructorJP() {
        return instructorJP;
    }

    public String getInstructorOOD() {
        return instructorOOD;
    }

    public String getInstructorST() {
        return instructorST;
    }

    public String getInstructorJ2EE() {
        return instructorJ2EE;
    }

    public String getInstructorSA() {
        return instructorSA;
    }

    public String getInstructorDP() {
        return instructorDP;
    }

    public void setRunNumber(String runNumber) {
        this.runNumber = runNumber;
    }

    public void setInstructorJP(String instructorJP) {
        this.instructorJP = instructorJP;
    }

    public void setInstructorOOD(String instructorOOD) {
        this.instructorOOD = instructorOOD;
    }

    public void setInstructorST(String instructorST) {
        this.instructorST = instructorST;
    }

    public void setInstructorJ2EE(String instructorJ2EE) {
        this.instructorJ2EE = instructorJ2EE;
    }

    public void setInstructorSA(String instructorSA) {
        this.instructorSA = instructorSA;
    }

    public void setInstructorDP(String instructorDP) {
        this.instructorDP = instructorDP;
    }

}
