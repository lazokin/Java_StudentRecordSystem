package view.io;

public class StudentFileEntry {

    private String id;
    private String name;
    private String year;
    private String resultJP;
    private String resultOOD;
    private String resultST;
    private String resultJ2EE;
    private String resultSA;
    private String resultDP;
    private String feePaid;

    public StudentFileEntry() {
        super();
    }

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getYear() {
        return year;
    }

    public String getResultJP() {
        return resultJP;
    }

    public String getResultOOD() {
        return resultOOD;
    }

    public String getResultST() {
        return resultST;
    }

    public String getResultJ2EE() {
        return resultJ2EE;
    }

    public String getResultSA() {
        return resultSA;
    }

    public String getResultDP() {
        return resultDP;
    }

    public String getFeePaid() {
        return feePaid;
    }

    public void setId(String id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setYear(String year) {
        this.year = year;
    }

    public void setResultJP(String resultJP) {
        this.resultJP = resultJP;
    }

    public void setResultOOD(String resultOOD) {
        this.resultOOD = resultOOD;
    }

    public void setResultST(String resultST) {
        this.resultST = resultST;
    }

    public void setResultJ2EE(String resultJ2EE) {
        this.resultJ2EE = resultJ2EE;
    }

    public void setResultSA(String resultSA) {
        this.resultSA = resultSA;
    }

    public void setResultDP(String resultDP) {
        this.resultDP = resultDP;
    }

    public void setFeePaid(String feePaid) {
        this.feePaid = feePaid;
    }

}
