package model;

/**
 * An interface for a person.
 * 
 * @author Nikolce Ambukovski, s2008618
 * 
 */
public interface Person {

    /**
     * Returns the id of the student.
     * 
     * @return A string representing the id of the student.
     */
    String getId();

    /**
     * Returns the name of the student.
     * 
     * @return A string representing the name of the student.
     */
    String getName();

}
